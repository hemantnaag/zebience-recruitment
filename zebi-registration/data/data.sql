DROP DATABASE IF EXISTS `REGISTRATION_TEST_DB`;
CREATE DATABASE `REGISTRATION_TEST_DB`;

USE `REGISTRATION_TEST_DB`;

DROP TABLE IF EXISTS `REGISTRATION`;
CREATE TABLE `REGISTRATION`(
    `REGISTRATIONID` INT NOT NULL PRIMARY KEY,
    `USERCODE` VARCHAR(40),
    `URL` VARCHAR(255),
    `TYPE` VARCHAR(40),
    `SERVICEID` INT,
    `EMAIL` VARCHAR(60),
    `APPROVALSTATUS` BOOLEAN,
    `REFERENCEID` INT
);